#pragma once

#define ALIAS_TEMPLATE_FUNCTION(highLevelF, lowLevelF)     \
  template <typename... Args>                              \
  inline auto highLevelF(Args&&... args)                   \
      ->decltype(lowLevelF(std::forward<Args>(args)...)) { \
    return lowLevelF(std::forward<Args>(args)...);         \
  }
