inline void ComputeConvsData(dbl::Para const& para,
                             dbl::UniData const& uni_data,
                             dbl::ConvsData& convs_data) {
  for (size_t k = 0; k < 5; ++k) {
    for (size_t i = 0; i < 26; ++i) {
      for (size_t j = 0; j < 26; ++j) {
        std::array<double, 9> view;
        view[0] = uni_data[i * 28 + j + 0];
        view[1] = uni_data[i * 28 + j + 1];
        view[2] = uni_data[i * 28 + j + 2];
        view[3] = uni_data[i * 28 + j + 28];
        view[4] = uni_data[i * 28 + j + 29];
        view[5] = uni_data[i * 28 + j + 30];
        view[6] = uni_data[i * 28 + j + 56];
        view[7] = uni_data[i * 28 + j + 57];
        view[8] = uni_data[i * 28 + j + 58];
        auto v =
            std::inner_product(view.begin(), view.end(),
                               para.conv[k].coef.begin(), para.conv[k].bias);
        // relu
        convs_data[k][i * 26 + j] = v >= 0 ? v : 0;
      }
    }
  }
}

// combine conv, relu, pooling, flatten
inline void R1csComputeConv(dbl::Para const& para, dbl::UniData const& uni_data,
                            std::array<double, 13 * 13 * 5>& poolings_data) {
  // public input
  std::array<std::array<double, (3 + 1) * (3 + 1)>, 13 * 13 * 5> public_inputs;
  for (size_t k = 0; k < 13 * 13; ++k) {
    auto& view = public_inputs[k * 5];
    size_t i = (k / 13) * 2;
    size_t j = (k % 13) * 2;

    for (size_t v = 0; v < view.size(); ++v) {
      size_t r = v / (3 + 1);
      size_t c = v % (3 + 1);
      view[v] = uni_data[i * 28 + j + r * 28 + c];
    }

    for (size_t ii = 1; ii < 5; ++ii) {
      public_inputs[k * 5 + ii] = view;
    }
  }

  // secret input
  std::array<dbl::ConvPara, 13 * 13 * 5> secret_inputs;
  for (size_t k = 0; k < 13 * 13; ++k) {
    for (size_t i = 0; i < 5; ++i) {
      secret_inputs[k * 5 + i] = para.conv[i];
    }
  }

  // output
  for (size_t k = 0; k < 13 * 13 * 5; ++k) {
    auto const& pub = public_inputs[k];
    auto const& sec = secret_inputs[k];
    std::array<double, 4> relu;

    std::array<double, 9> rect = {pub[0], pub[1], pub[2], pub[4], pub[5],
                                  pub[6], pub[8], pub[9], pub[10]};
    relu[0] =
        std::inner_product(rect.begin(), rect.end(), sec.coef.begin(), 0.0) +
        sec.bias;
    relu[0] = relu[0] >= 0 ? relu[0] : 0;

    rect = {pub[1], pub[2], pub[3],  pub[5], pub[6],
            pub[7], pub[9], pub[10], pub[11]};
    relu[1] =
        std::inner_product(rect.begin(), rect.end(), sec.coef.begin(), 0.0) +
        sec.bias;
    relu[1] = relu[1] >= 0 ? relu[1] : 0;

    rect = {pub[4],  pub[5],  pub[6],  pub[8], pub[9],
            pub[10], pub[12], pub[13], pub[14]};
    relu[2] =
        std::inner_product(rect.begin(), rect.end(), sec.coef.begin(), 0.0) +
        sec.bias;
    relu[2] = relu[2] >= 0 ? relu[2] : 0;

    rect = {pub[5],  pub[6],  pub[7],  pub[9], pub[10],
            pub[11], pub[13], pub[14], pub[15]};
    relu[3] = std::inner_product(rect.begin(), rect.end(), sec.coef.begin(),
                                 sec.bias);
    relu[3] = relu[2] >= 0 ? relu[3] : 0;
    poolings_data[k] = *std::max_element(relu.begin(), relu.end());
  }
}

inline void ComputePoolingsData(dbl::ConvsData const& convs_data,
                                dbl::PoolingsData& poolings_data) {
  for (size_t k = 0; k < 5; ++k) {
    for (size_t i = 0; i < 13; ++i) {
      for (size_t j = 0; j < 13; ++j) {
        std::array<double, 4> rect;
        auto ii = i * 2;
        auto jj = j * 2;
        rect[0] = convs_data[k][ii * 26 + jj];
        rect[1] = convs_data[k][ii * 26 + jj + 1];
        rect[2] = convs_data[k][ii * 26 + jj + 26];
        rect[3] = convs_data[k][ii * 26 + jj + 26 + 1];
        poolings_data[k][i * 13 + j] =
            *std::max_element(rect.begin(), rect.end());
      }
    }
  }
}

inline void ComputeFlatData(dbl::PoolingsData const& poolings_data,
                            dbl::FlatData& flat_data) {
  for (size_t j = 0; j < 5; ++j) {
    for (size_t i = 0; i < 169; ++i) {
      flat_data[i * 5 + j] = poolings_data[j][i];
    }
  }
}

inline void ComputeDense1Data(dbl::Para const& para,
                              dbl::FlatData const& flat_data,
                              dbl::Dense1Data& dense1_data) {
  for (size_t j = 0; j < 10; ++j) {
    auto& dense1 = dense1_data[j];
    dense1 =
        std::inner_product(flat_data.begin(), flat_data.end(),
                           para.dense1[j].coef.begin(), para.dense1[j].bias);
    dense1 = dense1 >= 0 ? dense1 : 0;
  }
}

inline void ComputeDense2Data(dbl::Para const& para,
                              dbl::Dense1Data const& dense1_data,
                              dbl::Dense2Data& dense2_data) {
  for (size_t j = 0; j < 10; ++j) {
    auto& dense2 = dense2_data[j];
    dense2 =
        std::inner_product(dense1_data.begin(), dense1_data.end(),
                           para.dense2[j].coef.begin(), para.dense2[j].bias);
  }
}

