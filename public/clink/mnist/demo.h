template <typename T>
void PrintTableRow(T const& t, const int width) {
  const char separator = ' ';
  std::cout << std::right << std::setw(width) << std::setfill(separator) << t;
}

template <typename T>
void PrintTableRowTitle(T const& title, const int width) {
  const char separator = ' ';
  std::cout << std::left << std::setw(width) << std::setfill(separator)
            << title;
}

